#import "DES3Util.h"
#import "AppConfig.h"
#import <CommonCrypto/CommonCryptor.h>
#import "GTBase64.h"

#define gkey			@"hnaction"

#define gIv             @"hnaction"
#define AES_KEY         @"abcdef1234567890"

static Byte iv[] =  { 0x12, 0x34, 0x56, 0x78, (Byte) 0x90,
    (Byte) 0xAB, (Byte) 0xCD, (Byte) 0xEF, 0x12, 0x34, 0x56, 0x78,
    (Byte) 0x90, (Byte) 0xAB, (Byte) 0xCD, (Byte) 0xEF };

@implementation DES3Util
+(NSString *)DESEncrypt:(NSString *)plainText
{
    char keyPtr[kCCKeySizeDES+1];
    memset(keyPtr, 0, sizeof(keyPtr));
    [gkey getCString:keyPtr maxLength:sizeof(keyPtr) encoding:NSUTF8StringEncoding];
    
    char ivPtr[kCCBlockSizeDES+1];
    memset(ivPtr, 0, sizeof(ivPtr));
    [gIv getCString:ivPtr maxLength:sizeof(ivPtr) encoding:NSUTF8StringEncoding];
    
    NSData* data = [plainText dataUsingEncoding:NSUTF8StringEncoding];
    NSUInteger dataLength = [data length];
    
    int diff = kCCKeySizeDES - (dataLength % kCCKeySizeDES);
    int newSize = 0;
    
    if(diff > 0)
    {
        newSize = dataLength + diff;
    }
    
    char dataPtr[newSize];
    memcpy(dataPtr, [data bytes], [data length]);
    for(int i = 0; i < diff; i++)
    {
        dataPtr[i + dataLength] = 0x00;
    }
    
    size_t bufferSize = newSize + kCCBlockSizeDES;
    void *buffer = malloc(bufferSize);
    memset(buffer, 0, bufferSize);
    
    size_t numBytesCrypted = 0;
    
    CCCryptorStatus cryptStatus = CCCrypt(kCCEncrypt,
                                          kCCAlgorithmDES,
                                          0x0000,               //No padding
                                          keyPtr,
                                          kCCKeySizeDES,
                                          ivPtr,
                                          dataPtr,
                                          sizeof(dataPtr),
                                          buffer,
                                          bufferSize,
                                          &numBytesCrypted);
    
    if (cryptStatus == kCCSuccess) {
        NSData *resultData = [NSData dataWithBytesNoCopy:buffer length:numBytesCrypted];
        return [GTBase64 stringByEncodingData:resultData];
    }
    free(buffer);
    return nil;
}

